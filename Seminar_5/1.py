# ✔Пользователь вводит строку из четырёх или более целых чисел, разделённых символом “/”.
# Сформируйте словарь, где: ✔второе и третье число являются ключами.
# ✔первое число является значением для первого ключа. ✔четвертое и все возможные
# последующие числа хранятся в кортеже как значения второго ключа.

def myDict(text):
    a, b, c, *d = text.split('/')
    return {b: a, c: d}

print(myDict(input()))
