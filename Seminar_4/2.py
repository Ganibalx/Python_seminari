# ✔Напишите функцию, которая принимает строку текста.
# ✔Сформируйте список с уникальными кодами Unicode каждого символа введённой строки отсортированный по убыванию.


def unicode(text):
    for i in sorted([ord(j) for j in set(text)], reverse=True):
        print(i)

unicode('тут будет текст, который мы выведем в результате')

